package javabean;

public class Staff {
	
	private String name;
	private String nickName;
	public Staff() {
		super();
		this.name="SURIA";
		this.nickName="ABSENT MINDED";
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNick() {
		return nickName;
	}
	public void setNick(String nickName) {
		this.nickName = nickName;
	}
	
	

}
